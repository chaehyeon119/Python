'''
대부분의 언어도 이러한 대응 관계를 나타내는 자료형을 갖고 있는데,
이를 연관 배열(Associative array)또는 해시(Hash)라고 한다.
파이썬에서는 이러한 자료형을 딕셔너리라고 한다.
딕셔너리는 중괄호{}이용한다.
딕셔너리는 Key와 Value를 한 쌍으로 갖는 자료형
'''

a={1:'hi'}
print(a) #{1:'hi'}
print('-'*15)
a={'a':[1,2,3]}
print(a) #{'a':[1,2,3]}

print('-'*15)
